package editor.enums;

/**
 * Nom des attributs d'une énigme
 */
public enum EnigmaAttributes {

    /**
     * Chemin de la classe
     */
    PATH,

    /**
     * Indice
     */
    TITLE,

    /**
     * Délai
     */
    DESCRITPION,

    /**
     * L'énimge à été découverte ou nom
     */
    KNOWN,

    /**
     * Index désignant l'indice actuel
     */
    CURRENT_ADVICE_INDEX
}

